static void uigfx_draw_char_font8x8_vic(uint8_t* buf, int16_t x0, int16_t y0, char ch, uigfx_color_t c)
{
    uint8_t x,y;
    uint8_t* chdat = (uint8_t*)uigfx_font8x8_vic_data[ch-32];

    for(y=0; y < 8; y++)
    {
        uint8_t rowdat = chdat[y];

        for(x=0; x < 8; x++)
        {
            if(rowdat&1)
            {
                uigfx_draw_pixel(buf,x0+x,y0+y,c);
            }

            rowdat>>=1;
        }
    }
}

const uigfx_font_t uigfx_font8x8_vic = {8,8,uigfx_font8x8_vic_data,uigfx_draw_char_font8x8_vic};
